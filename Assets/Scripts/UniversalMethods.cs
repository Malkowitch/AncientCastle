using System.Collections;
using System.Collections.Generic;

public class UniversalMethods  {

    /// <summary>
    /// Returns the opposite of the inserted bool.
    /// </summary>
    /// <param name="_toBool">Insert the bool to be converted</param>
    /// <returns>Input true = flase. Input false = true.</returns>
    public static bool ReBoolean(bool _toBool)
    {
        if (_toBool)
            return false;
        return true;
    }



}
